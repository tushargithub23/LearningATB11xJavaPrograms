public class Forloop
{
    public static void main(String[] args)
    {
        System.out.println("Loops");
        for(int i=1;i<=10;i++)
        {
            //System.out.println();
            for(int j=1;j<=10;j++)
            {
                System.out.println(i*j);
            }
            System.out.println();
        }



    }

}
